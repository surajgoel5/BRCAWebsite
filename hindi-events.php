                    <h1>Andaaz-e-Bayan</h1>
                    <p>
                    One of the side effects of our globalising world is that the  Indian youth are drifting away from  our heritage, our culture. This is even truer  for our language. Basically in Indian colleges where glamour reigns supreme often Hindi and Urdu are left behind. Apart from a few mandatory rituals these languages are fast becoming obsolete in the college culture of today's India.</p>
                    <p>In this background the credit goes to Hindi Damiti IIT Delhi to turesake an initiative like Andaaz–e-Bayan  in the historic city of Delhi.</p>
                    <p>Andaaz-e-Bayan is a stage where eagerness will meet calmness, fire and love will dance in sync, where the young will be mesmerized by the poetic exellence  of the young. Where those whose hairs have greyed with experience will find a poetic repose. Where we at IIT Delhi will celebrate, salute and cherish our Indian culture.</p>
