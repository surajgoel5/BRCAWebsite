                    <h1>Varg Paheli (Crossword)</h1>
                    <p>Varg Paheli (Crossword), a fun event organised by Hindi Samiti, sees a great participation of IIT freshers. Questions/Hints  given to solve crossword are not just limited to hindi but also involve interesting  topics like Bollywood, Mythology, Hindi Literature, Poetry, Sports etc. which makes this  event quite popular. Since questions are asked from a large no. of fields, there is always something for everybody.</p>
